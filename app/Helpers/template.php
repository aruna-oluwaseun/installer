/**
 * Get video ID
 */
if(!function_exists('video_id'))
{
    function video_id($url)
    {

        // Youtube
        if(preg_match("/^(?:http(?:s)?:\/\/)?(?:www\.)?(?:m\.)?(?:youtu\.be\/|youtube\.com\/(?:(?:watch)?\?(?:.*&)?v(?:i)?=|(?:embed|v|vi|user)\/))([^\?&\"'>]+)/", $url, $matches))
        {
            return $matches[1];
        }

        // Vimeo
        if(preg_match("/(https?:\/\/)?(www\.)?(player\.)?vimeo\.com\/([a-z]*\/)*([0-9]{6,11})[?]?.*/", $url, $matches)) {
            return $matches[5];
        }

        return false;
    }
}

/**
 * Embed video
 */
if(!function_exists('embed_video'))
{
    function embed_video($url)
    {

        // Youtube
        if(preg_match("/^(?:http(?:s)?:\/\/)?(?:www\.)?(?:m\.)?(?:youtu\.be\/|youtube\.com\/(?:(?:watch)?\?(?:.*&)?v(?:i)?=|(?:embed|v|vi|user)\/))([^\?&\"'>]+)/", $url, $matches))
        {
            $video_id = $matches[1];
            return '<iframe src="https://www.youtube.com/embed/'.$video_id.'" allowfullscreen></iframe>';
        }

        // Vimeo
        if(preg_match("/(https?:\/\/)?(www\.)?(player\.)?vimeo\.com\/([a-z]*\/)*([0-9]{6,11})[?]?.*/", $url, $matches))
        {
            $video_id = $matches[5];
            return '<iframe src="https://player.vimeo.com/video/'.$video_id.'" allow="autoplay; fullscreen; picture-in-picture" allowfullscreen></iframe>';
        }

        return false;
    }
}

