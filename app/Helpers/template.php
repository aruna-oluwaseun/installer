if(!function_exists('is_active_menu'))
{
    function is_active_menu($menu, $wildcard = false) : bool
    {
        global $tpl_active_menu;
        if(!$tpl_active_menu) {
            $tpl_active_menu = 'home'; // default
        }

        if($wildcard) {
            $links = (array) explode('/',$tpl_active_menu);
            $check = (array) explode('/',$menu);

            return $links[0] == $check[0];
        }

        return $tpl_active_menu == $menu;
    }
}



